'''
\n    creates a line return in a string
\t    creates a tab style indent in a string
\\    allows a backslash to appear in string
\"    allows a speech mark to appear in a string
'''
my_string = "\"Hi Mia,\" said Daniel."
print(my_string)

my_string = '"Hi Mia," said Daniel.'
print(my_string)