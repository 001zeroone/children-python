# if elif else
# if
my_number = 7
if my_number > 5:
    print("My number is big.")

# else
my_number = 2
if my_number > 5:
    print("My number is big.")
else:
    print("My number is small.")

# elif
my_number = 7
if my_number < 5:
    print("My number is small.")
elif my_number < 10:
    print("My number is medium sized.")
elif my_number < 100:
    print("My number is big.")
else:
    print("My number is huge.")
