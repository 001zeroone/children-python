'''
程序：二果问价
作者：苏秦@小海豚科学馆公众号
来源：图书《Python趣味编程：从入门到人工智能》

根据题意得出两个等式：
    x + y = 1000
    11 * x / 9 + 4 * y / 7 = 999
采用枚举法，用单重循环列举x的值，y的值可由等量关系计算得到。
判断如果等式成立，则可求得答案。
'''
def main():
    '''二果问价'''
    x = 1
    while True:
        y = 1000 - x
        if 11 * x / 9 + 4 * y / 7 == 999:
            print('果:', x, y)
            print('钱:', 11 * x // 9, 4 * y // 7)
            break
        else:
            x = x + 1

if __name__ == '__main__':
    main()
